#Récupération variable choix en nombre entier pour repondre à un choix
choix = int(input("Nom:1 Nb_entier:2 Nb_decimal:3 "))
if choix == 1 :
#Récuperation de la variable nom en chaine de caractère (blockchain)
    nom = input("Quel est votre nom : ")
    print(nom)
elif choix == 2 :
#Récuperation variable nb_entier en nombre entier (int)
    nb_entier = int(input("Saisissez nombre :"))
    nb_entier += 10
    print(nb_entier)
elif  choix == 3:
#Récuperation de la variable nb_decimal en nombre decimal (float)
    nb_decimal = float(input("Saisissez nombre :"))
    nb_decimal += 10.32
    print(nb_decimal)
else :
    print(" Erreur : choix non valable")













































































































































































































































































































































































































































